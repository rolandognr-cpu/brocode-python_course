# Typecasting is the process of converting a variable from one data type to another
#               str(), int(), float(), bool()


name = "Bro"
age = 21
gpa = 1.9
student = True

print(type(name))
print(type(age))
print(type(gpa))
print(type(student))
print()

new_age = float(age)
print(new_age)

new_gpa = int(gpa)
print(new_gpa)

new_student = str(student)
print(new_student)
print(type(new_student))